//Print All Even numbers in an Array

let arr = [10, 6, 2, 0, 8, 3, 80];

for(let i=0; i<arr.length; i++){
    if(arr[i]%2 == 0){
        console.log("Even Numbers",arr[i])
    }
}

//Print All Odd numbers in an Array

let array = [10, 6, 2, 0, 8, 3, 80];

for(let i=0; i<array.length; i++){
    if(arr[i]%2 == 1){
        console.log("Odd Numbers",array[i])
    }
}